"""Experiment-induced quotients for finite ecological evidence designs.

A declared experiment does not generally identify one latent ecological world. It
partitions worlds by the records it can produce. Exact deterministic reporting is
possible precisely when the requested report target is constant inside every
record class. Otherwise the honest record-based report is set-valued.
"""

from __future__ import annotations

from dataclasses import dataclass
from typing import Hashable, Iterable

World = Hashable
Record = Hashable
Target = Hashable


def _tuple(name: str, values: Iterable[Hashable]) -> tuple[Hashable, ...]:
    result = tuple(values)
    if not result:
        raise ValueError(f"{name} must be nonempty")
    for value in result:
        try:
            hash(value)
        except TypeError as error:
            raise ValueError(f"{name} values must be hashable") from error
    return result


@dataclass(frozen=True)
class ExperimentInducedQuotient:
    """Finite latent worlds partitioned by a declared experiment record."""

    worlds: tuple[World, ...]
    records: tuple[Record, ...]
    targets: tuple[Target, ...]

    def __post_init__(self) -> None:
        worlds = _tuple("worlds", self.worlds)
        records = _tuple("records", self.records)
        targets = _tuple("targets", self.targets)
        if len(set(worlds)) != len(worlds):
            raise ValueError("worlds must be unique")
        if len(records) != len(worlds) or len(targets) != len(worlds):
            raise ValueError("records and targets must align with worlds")
        object.__setattr__(self, "worlds", worlds)
        object.__setattr__(self, "records", records)
        object.__setattr__(self, "targets", targets)

    @classmethod
    def from_functions(
        cls,
        worlds: Iterable[World],
        record_function,
        target_function,
    ) -> "ExperimentInducedQuotient":
        world_tuple = tuple(worlds)
        return cls(
            world_tuple,
            tuple(record_function(world) for world in world_tuple),
            tuple(target_function(world) for world in world_tuple),
        )

    @property
    def record_classes(self) -> tuple[tuple[World, ...], ...]:
        classes: list[list[World]] = []
        record_to_index: dict[Record, int] = {}
        for world, record in zip(self.worlds, self.records):
            if record not in record_to_index:
                record_to_index[record] = len(classes)
                classes.append([])
            classes[record_to_index[record]].append(world)
        return tuple(tuple(group) for group in classes)

    @property
    def class_records(self) -> tuple[Record, ...]:
        seen: set[Record] = set()
        result: list[Record] = []
        for record in self.records:
            if record not in seen:
                seen.add(record)
                result.append(record)
        return tuple(result)

    @property
    def quotient_state_count(self) -> int:
        return len(self.record_classes)

    @property
    def residual_class_sizes(self) -> tuple[int, ...]:
        return tuple(len(group) for group in self.record_classes)

    @property
    def deterministic_report_exists(self) -> bool:
        return all(len(self.targets_for_record(record)) == 1 for record in self.class_records)

    def class_of(self, world: World) -> int:
        try:
            record = self.records[self.worlds.index(world)]
        except ValueError as error:
            raise ValueError("unknown latent world") from error
        return self.class_records.index(record)

    def worlds_for_record(self, record: Record) -> tuple[World, ...]:
        worlds = tuple(
            world
            for world, candidate_record in zip(self.worlds, self.records)
            if candidate_record == record
        )
        if not worlds:
            raise ValueError("record is not generated by the declared design")
        return worlds

    def targets_for_record(self, record: Record) -> frozenset[Target]:
        targets = frozenset(
            target
            for target, candidate_record in zip(self.targets, self.records)
            if candidate_record == record
        )
        if not targets:
            raise ValueError("record is not generated by the declared design")
        return targets

    def exact_report_for_record(self, record: Record) -> Target:
        targets = self.targets_for_record(record)
        if len(targets) != 1:
            raise ValueError("target is not constant on this record class")
        return next(iter(targets))

    def set_valued_report_for_record(self, record: Record) -> frozenset[Target]:
        return self.targets_for_record(record)

    def refines(self, other: "ExperimentInducedQuotient") -> bool:
        """Whether this design separates every pair separated by ``other``."""
        if self.worlds != other.worlds:
            raise ValueError("quotients must use the same ordered latent worlds")
        return all(
            self.records[left] != self.records[right]
            or other.records[left] == other.records[right]
            for left in range(len(self.worlds))
            for right in range(len(self.worlds))
        )

    def verify(self) -> bool:
        classes = self.record_classes
        flattened = tuple(world for group in classes for world in group)
        return (
            set(flattened) == set(self.worlds)
            and len(flattened) == len(self.worlds)
            and self.quotient_state_count == len(self.class_records)
            and all(size > 0 for size in self.residual_class_sizes)
        )


@dataclass(frozen=True)
class CompatibleRecordReport:
    """Bounded-support observation report over compatible latent worlds."""

    worlds: tuple[World, ...]
    targets: tuple[Target, ...]
    compatible_worlds: tuple[World, ...]

    def __post_init__(self) -> None:
        worlds = _tuple("worlds", self.worlds)
        targets = _tuple("targets", self.targets)
        compatible = _tuple("compatible_worlds", self.compatible_worlds)
        if len(worlds) != len(targets):
            raise ValueError("targets must align with worlds")
        if len(set(worlds)) != len(worlds):
            raise ValueError("worlds must be unique")
        if len(set(compatible)) != len(compatible) or any(world not in worlds for world in compatible):
            raise ValueError("compatible_worlds must be a nonempty subset of worlds")
        object.__setattr__(self, "worlds", worlds)
        object.__setattr__(self, "targets", targets)
        object.__setattr__(self, "compatible_worlds", compatible)

    @property
    def compatible_targets(self) -> frozenset[Target]:
        target_by_world = dict(zip(self.worlds, self.targets))
        return frozenset(target_by_world[world] for world in self.compatible_worlds)

    @property
    def deterministic_report_exists(self) -> bool:
        return len(self.compatible_targets) == 1
