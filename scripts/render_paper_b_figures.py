"""Generate deterministic LaTeX/TikZ figures from the Paper B benchmark.

The script regenerates the benchmark in memory and writes figure fragments used by
``manuscript/paper_b_main.tex``. No plotting dependency is required.
"""
from __future__ import annotations

import runpy
from pathlib import Path

ROOT = Path(__file__).resolve().parents[1]
BENCHMARK = ROOT / "scripts" / "simulate_paper_b_benchmark.py"
OUT_DIR = ROOT / "manuscript" / "generated"
OUT_CONTRAST = OUT_DIR / "paper_b_experiment_contrast.tex"
OUT_OUTCOMES = OUT_DIR / "paper_b_strategy_outcomes.tex"

STRATEGY_LABELS = {
    "state_only": "State-only",
    "full_identification": "Full identification",
    "full_world_eig": "Full-world EIG",
    "target_safe": "Target-safe",
}


def _fmt(value: float) -> str:
    return f"{value:.6f}"


def _baseline_rows(report: dict[str, object]) -> list[dict[str, object]]:
    rows = [
        row
        for row in report["rows"]
        if row["architecture"] == "independent"
        and row["state_detection_sensitivity"] == 0.95
        and row["response_typing_accuracy"] == 0.99
        and row["common_failure_probability"] == 0.0
    ]
    order = tuple(STRATEGY_LABELS)
    return sorted(rows, key=lambda row: order.index(row["strategy"]))


def experiment_contrast_tex(report: dict[str, object]) -> str:
    contrast = report["benchmark_contrast"]
    gains = contrast["full_world_information_gain_bits"]
    resolutions = contrast["target_resolution_probability"]
    return rf"""% Generated by scripts/render_paper_b_figures.py; do not edit.
\begin{{tikzpicture}}[font=\small]
  \begin{{axis}}[
    ybar,
    bar width=18pt,
    width=0.82\linewidth,
    height=6.2cm,
    ymin=0,
    ymax=2.15,
    ylabel={{Information or target resolution}},
    symbolic x coords={{Response experiment,Nuisance experiment}},
    xtick=data,
    x tick label style={{align=center}},
    legend style={{at={{(0.5,-0.23)}},anchor=north,legend columns=2}},
    nodes near coords,
    nodes near coords style={{font=\scriptsize}},
  ]
    \addplot coordinates {{(Response experiment,{_fmt(gains['response'])}) (Nuisance experiment,{_fmt(gains['nuisance'])})}};
    \addplot coordinates {{(Response experiment,{_fmt(resolutions['response'])}) (Nuisance experiment,{_fmt(resolutions['nuisance'])})}};
    \legend{{Full-world information gain (bits),Target-resolution probability}}
  \end{{axis}}
\end{{tikzpicture}}
"""


def strategy_outcomes_tex(report: dict[str, object]) -> str:
    rows = _baseline_rows(report)
    coordinates = {metric: [] for metric in ("correct_probability", "wrong_probability", "ambiguity_probability")}
    for row in rows:
        label = STRATEGY_LABELS[row["strategy"]]
        for metric in coordinates:
            coordinates[metric].append(f"({{{label}}},{_fmt(row[metric])})")
    return rf"""% Generated by scripts/render_paper_b_figures.py; do not edit.
\begin{{tikzpicture}}[font=\small]
  \begin{{axis}}[
    ybar stacked,
    bar width=22pt,
    width=0.96\linewidth,
    height=7cm,
    ymin=0,
    ymax=1.02,
    ylabel={{Terminal-report probability}},
    symbolic x coords={{{','.join(STRATEGY_LABELS.values())}}},
    xtick=data,
    x tick label style={{rotate=20,anchor=east}},
    legend style={{at={{(0.5,-0.34)}},anchor=north,legend columns=3}},
  ]
    \addplot coordinates {{{' '.join(coordinates['correct_probability'])}}};
    \addplot coordinates {{{' '.join(coordinates['wrong_probability'])}}};
    \addplot coordinates {{{' '.join(coordinates['ambiguity_probability'])}}};
    \legend{{Correct deterministic,Wrong deterministic,Honest ambiguity}}
  \end{{axis}}
\end{{tikzpicture}}
"""


def write_figures() -> dict[str, object]:
    module = runpy.run_path(str(BENCHMARK))
    report = module["run_grid"]()
    if report["schema_version"] != 5:
        raise RuntimeError("figure generator requires benchmark schema version 5")
    OUT_DIR.mkdir(parents=True, exist_ok=True)
    OUT_CONTRAST.write_text(experiment_contrast_tex(report), encoding="utf-8")
    OUT_OUTCOMES.write_text(strategy_outcomes_tex(report), encoding="utf-8")
    return report


def main() -> None:
    report = write_figures()
    print(
        {
            "schema_version": report["schema_version"],
            "outputs": [str(OUT_CONTRAST.relative_to(ROOT)), str(OUT_OUTCOMES.relative_to(ROOT))],
        }
    )


if __name__ == "__main__":
    main()
